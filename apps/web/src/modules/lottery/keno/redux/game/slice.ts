export const KEY_REDUCER_SAGA = '@keno/game'

const kenoGameSlice = createSlice({
    name: KEY_REDUCER_SAGA,
    initialState,
    reducers: {
        onInitTicket: initTicket,
        onFetchDraw: fetchDraw,
        onSetDraw: setDraw,
        onSetDrawMapByDate: setDrawMapByDate,
        onSetDrawingConfig: setDrawingConfig,
        onSetTicketPriceDefault: setTicketPriceDefault,
        onRemoveDrawSelected: removeDrawSelected,
        onSetDrawSelected: setDrawSelected,
        onSetDrawSelectedAll: setDrawSelectedAll,
        onRemoveDrawSelectedAll: removeDrawSelectedAll,
        onSetDrawSelectedList: setDrawSelectedList,
        onInitNumbers: initNumbers,
        onSetNumbers: setNumbers,
        onInitSpots: initSpots,
        onSetSpots: setSpots,
        onSetSpotSelected: setSpotSelected,
        onSetSpotDefault: setSpotDefault,
        onSetTicketOrder: setTicketOrder,
        onResetTicketOrderNumber: resetTicketOrderNumber,
        onSetTicketSessionSelected: setTicketSessionSelected,
        onResetTicketSession: resetTicketSession,
        onRandomNumberTicket: randomNumberTicket,
        onUpdateTicketOrderNumber: updateTicketOrderNumber,
        onAddTicket: addTicket,
        onRemoveTicket: removeTicket,
        onClearNumberTicket: clearNumberTicket,

        onSetTicketSession: setTicketSession,
        onUpdateSessionItem: updateSessionItem,
        onRemoveSessionItem: removeSessionItem,
        onClearNumberSessionItem: clearNumberSessionItem,
        onMergeTicketOrderFromSession: mergeTicketOrderFromSession,

        onFetchConfig: fetchConfig,
        onGetConfig: getConfig,

        onSetNumberChoiceModal: setNumberChoiceModal,
        onCreateOrder: createOrder,
        onCreateOrderLoading: createOrderLoading
    }
})

