    initTicket,
    fetchDraw,
    setDraw,
    setDrawMapByDate,
    setDrawingConfig,
    setTicketPriceDefault,
    removeDrawSelected,
    setDrawSelected,
    setDrawSelectedAll,
    removeDrawSelectedAll,
    setDrawSelectedList,
    initNumbers,
    setNumbers,
    initSpots,
    setSpots,
    setSpotSelected,
    setSpotDefault,
    setTicketOrder,
    resetTicketOrderNumber,
    setTicketSessionSelected,
    resetTicketSession,
    randomNumberTicket,
    updateTicketOrderNumber,
    addTicket,
    removeTicket,
    clearNumberTicket,
